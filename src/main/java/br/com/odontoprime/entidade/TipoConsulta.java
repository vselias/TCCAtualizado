package br.com.odontoprime.entidade;

public enum TipoConsulta {

	CANAL("Canal"), CIRURIGIA("Cirurgia"), CLAREAMENTO("Clareamento"), EXTRACAO("Extração"), LIMPEZA(
			"Limpeza"), RETORNO("Retorno"), MONTAGEM_DE_APARELHO("Montagem de Aparelho"), MANUTENCAO("Manutenção");

	private String descricao;

	public String getDescricao() {
		return descricao;
	}

	private TipoConsulta(String descricao) {
		this.descricao = descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

}
